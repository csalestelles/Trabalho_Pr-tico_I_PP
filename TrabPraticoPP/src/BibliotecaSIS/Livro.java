package BibliotecaSIS;

public class Livro {
	
	private String titulo, editora, autor, sinopse, idioma;
	private int ano, edicao, numExemplaresDisponiveis;
	
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getEditora() {
		return editora;
	}
	public void setEditora(String editora) {
		this.editora = editora;
	}
	public String getAutor() {
		return autor;
	}
	public void setAutor(String autor) {
		this.autor = autor;
	}
	public String getSinopse() {
		return sinopse;
	}
	public void setSinopse(String sinopse) {
		this.sinopse = sinopse;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public int getEdicao() {
		return edicao;
	}
	public void setEdicao(int edicao) {
		this.edicao = edicao;
	}
	public int getNumExemplaresDisponiveis() {
		return numExemplaresDisponiveis;
	}
	public void setNumExemplaresDisponiveis(int numExemplaresDisponiveis) {
		this.numExemplaresDisponiveis = numExemplaresDisponiveis;
	}
	public String getIdioma() {
		return idioma;
	}
	public void setIdioma(String idioma) {
		this.idioma = idioma;
	}
	
	
	

}
